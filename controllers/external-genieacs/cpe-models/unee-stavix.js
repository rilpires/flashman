const basicCPEModel = require('./base-model');

let uneeModel = {};

uneeModel.identifier = 'Unee Stavix G421R';

uneeModel.modelPermissions = function() {
  let permissions = basicCPEModel.modelPermissions();
  permissions.features.pingTest = true;
  permissions.features.ponSignal = true;
  permissions.features.speedTest = true;
  permissions.wan.speedTestLimit = 300;
  permissions.usesStavixXMLConfig = true;
  permissions.firmwareUpgrades = {
    'V1.2.9': [],
    'V1.3.4': [],
  };
  return permissions;
};

uneeModel.getFieldType = basicCPEModel.getFieldType;

uneeModel.convertWifiMode = function(mode) {
  switch (mode) {
    case '11g':
      return 'bg';
    case '11n':
      return 'bgn';
    case '11na':
      return 'an';
    case '11ac':
      return 'anac';
    case '11ax':
    default:
      return '';
  }
};

uneeModel.convertWifiBand = basicCPEModel.convertWifiBand;

uneeModel.convertWifiBandToFlashman = basicCPEModel.convertWifiBandToFlashman;

uneeModel.convertField = basicCPEModel.convertField;

uneeModel.getBeaconType = basicCPEModel.getBeaconType;

uneeModel.convertGenieSerial = basicCPEModel.convertGenieSerial;

uneeModel.convertToDbm = basicCPEModel.convertToDbm;

uneeModel.isAllowedWebadminUsername = function(name) {
  return (name !== 'admin');
};

uneeModel.getModelFields = function() {
  let fields = basicCPEModel.getModelFields();
  fields.wan.recv_bytes = 'InternetGatewayDevice.WANDevice.1.'+
    'WANCommonInterfaceConfig.TotalBytesReceived';
  fields.wan.sent_bytes = 'InternetGatewayDevice.WANDevice.1.'+
    'WANCommonInterfaceConfig.TotalBytesSent';
  fields.wan.pon_rxpower = 'InternetGatewayDevice.WANDevice.1.'+
    'X_GponInterafceConfig.RXPower';
  fields.wan.pon_txpower = 'InternetGatewayDevice.WANDevice.1.'+
    'X_GponInterafceConfig.TXPower';
  Object.keys(fields.wifi2).forEach((k)=>{
    fields.wifi2[k] = fields.wifi5[k].replace(/5/g, '6');
    fields.wifi5[k] = fields.wifi5[k].replace(/5/g, '1');
  });
  Object.keys(fields.mesh2).forEach((k)=>{
    fields.mesh2[k] = fields.mesh5[k].replace(/6/g, '7');
    fields.mesh5[k] = fields.mesh5[k].replace(/6/g, '2');
  });
  return fields;
};

module.exports = uneeModel;
